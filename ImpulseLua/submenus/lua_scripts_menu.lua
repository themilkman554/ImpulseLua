--- Resolve eFeatureType value to string (Button, Toggle, etc.).
local function typeToString(t)
    if type(t) == "string" then return t end
    if eFeatureType then
        for k, v in pairs(eFeatureType) do
            if v == t then return k end
        end
    end
    return tostring(t)
end

--- Scan TranslationFile and build dynamic menus based on unique features
local function BuildLuaMenus()
    Script.QueueJob(function()
        if not instance then return end

        -- Trigger feature callback and yield before building
        FeatureMgr.TriggerFeatureCallback(1675650873)
        Script.Yield(4000)

        local basePath = FileMgr.GetMenuRootPath() .. "\\Lua\\Impulse"
        -- New path: ...\Translations\TranslationFile.json
        local translationPath = FileMgr.GetMenuRootPath() .. "\\Translations\\TranslationFile.json"
        
        -- Use embedded exclusion list
        local discardHashes = {}
        if LuaScriptsMenu.TranslationExcludeList then
            for hash in pairs(LuaScriptsMenu.TranslationExcludeList) do
                discardHashes[hash] = true
            end
        end

        local translationRaw = FileMgr.ReadFileContent(translationPath)
        if not translationRaw or translationRaw == "" then
            Renderer.Notify("Lua Build: Could not read TranslationFile.json")
            return
        end
        local ok, translation = pcall(json.decode, translationRaw)
        if not ok or type(translation) ~= "table" then
            Renderer.Notify("Lua Build: Failed to parse TranslationFile.json")
            return
        end

        -- Clear existing dynamic options
        if instance.buttonSubmenu then instance.buttonSubmenu:ClearOptions() end
        if instance.toggleSubmenu then instance.toggleSubmenu:ClearOptions() end

        -- Build unique list
        local unique = {}
        for hash, name in pairs(translation) do
            -- Ensure we check as string, since TranslationExcludeList keys are strings
            if not discardHashes[tostring(hash)] then
                table.insert(unique, { hash = hash, name = name })
            end
        end
        table.sort(unique, function(a, b) return tostring(a.hash) < tostring(b.hash) end)

        local count = 0
        if FeatureMgr and FeatureMgr.GetFeatureByName then
            for _, entry in ipairs(unique) do
                local name = entry.name
                if name and name ~= "" then
                    local feature = FeatureMgr.GetFeatureByName(name)
                    
                    if feature then
                         local okType, t = pcall(function() return feature:GetType() end)
                         
                         if okType then
                             local typeStr = typeToString(t)
                             local desc = ""
                             pcall(function() desc = feature:GetDesc() end)

                             if typeStr == "Button" then
                                 pcall(function()
                                     instance.buttonSubmenu:AddOption(ButtonOption.new(name)
                                         :AddFunction(function() feature:TriggerCallback() end)
                                         :AddTooltip(desc)
                                     )
                                     count = count + 1
                                 end)
                             elseif typeStr == "Toggle" then
                                 pcall(function()
                                     local opt = ToggleOption.new(name)
                                     -- Sync state
                                     opt:AddOnUpdate(function(self)
                                         self.value = feature:IsToggled()
                                     end)
                                     -- Trigger toggle
                                     opt:AddFunction(function()
                                         feature:Toggle()
                                     end)
                                     opt:AddTooltip(desc)
                                     
                                     instance.toggleSubmenu:AddOption(opt)
                                     count = count + 1
                                 end)
                             end
                         end
                    end
                end
            end
        end

        -- Update visibility of submenu options
        if instance.buttonSubmenuOption then
            instance.buttonSubmenuOption.visible = (#instance.buttonSubmenu.options > 0)
        end
        if instance.toggleSubmenuOption then
            instance.toggleSubmenuOption.visible = (#instance.toggleSubmenu.options > 0)
        end

        if count > 0 then
            Renderer.Notify("Populated " .. count .. " Lua options.")
        else
            Renderer.Notify("No Lua options found.")
        end

         -- Also trigger build for Player Lua Content menu
        pcall(function()
            local PlayerLuaContentMenu = require("Impulse/ImpulseLua/submenus/session/player/player_lua_content_menu")
            if PlayerLuaContentMenu and PlayerLuaContentMenu.BuildMenus then
                PlayerLuaContentMenu.BuildMenus()
            end
        end)
    end)
end

function LuaScriptsMenu.GetInstance()
    if not instance then
        instance = setmetatable(Submenu.new("Lua Scripts"), LuaScriptsMenu)
        instance:Init()
    end
    return instance
end

function LuaScriptsMenu:Init()
    -- Create submenus
    self.buttonSubmenu = Submenu.new("Button Features")
    self.toggleSubmenu = Submenu.new("Toggle Features")

    -- Lua Script Loader
    local ScrollOption = require("Impulse/ImpulseLua/lib/options/scroll")
    self.scriptScroll = ScrollOption.new(ScrollOption.Type.SCROLL, "Choose Script")
        :AddIndexRef(scriptLoaderState, "selectedScriptIndex")
        :AddFunction(function()
            local index = scriptLoaderState.selectedScriptIndex
            local scriptFeature = FeatureMgr.GetFeature(3331055146)
            if scriptFeature then
                scriptFeature:SetListIndex(index - 1)
            end
        end)
        :AddTooltip("Select a Lua script to load")
    self:AddOption(self.scriptScroll)

    self:AddOption(ButtonOption.new("Run Script")
        :AddOnUpdate(function(self)
            self.name = scriptLoaderState.isScriptRunning and "Stop Script" or "Run Script"
        end)
        :AddFunction(function()
            Script.QueueJob(function()
                local index = scriptLoaderState.selectedScriptIndex
                local scriptFeature = FeatureMgr.GetFeature(3331055146)
                if scriptFeature then
                    scriptFeature:SetListIndex(index - 1)
                end
                
                Script.Yield(1000)
                
                if scriptLoaderState.isScriptRunning then
                    local stopFeature = FeatureMgr.GetFeature(2425713991)
                    if stopFeature then
                        stopFeature:OnClick()
                        scriptLoaderState.isScriptRunning = false
                    else
                        Renderer.Notify("~r~Error: Could not find Stop Script feature")
                    end
                else
                    local runFeature = FeatureMgr.GetFeature(2423908032)
                    if runFeature then
                        runFeature:OnClick()
                        scriptLoaderState.isScriptRunning = true
                    else
                        Renderer.Notify("~r~Error: Could not find Run Script feature")
                    end
                end
            end)
        end)
        :AddTooltip("Run or Stop the selected Lua script"))

    self:AddOption(ButtonOption.new("Lua Build")
        :AddFunction(BuildLuaMenus)
        :AddTooltip("Read translation files and build dynamic menus for non-player features"))

    self:AddOption(BreakOption.new())

    -- Store option references for visibility toggling
    self.buttonSubmenuOption = SubmenuOption.new("Buttons")
        :AddSubmenu(self.buttonSubmenu)
        :AddTooltip("Function buttons")
    self.buttonSubmenuOption.visible = false -- Start hidden
    self:AddOption(self.buttonSubmenuOption)

    self.toggleSubmenuOption = SubmenuOption.new("Toggles")
        :AddSubmenu(self.toggleSubmenu)
        :AddTooltip("Toggle features")
    self.toggleSubmenuOption.visible = false -- Start hidden
    self:AddOption(self.toggleSubmenuOption)
end

function LuaScriptsMenu:OnEnter()
    -- Refresh script list from Cherax
    if FeatureMgr and FeatureMgr.GetFeatureList then
        local list = FeatureMgr.GetFeatureList(3331055146)
        if list then
            scriptLoaderState.scripts = list
            if self.scriptScroll then
                self.scriptScroll:AddScroll(list, scriptLoaderState.selectedScriptIndex)
            end
        end
    end
end


